import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Salman Fiqi — Software Engineer",
  description:
    "Distributed systems, data infrastructure, and AI-adjacent tooling — LinkedIn intern, ColorStack VP, builder.",
};

// Runs before hydration to avoid flash of wrong theme
const themeScript = `(function(){var t=localStorage.getItem('portfolio-theme');if(t==='light')document.documentElement.classList.add('light');})();`

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html
      lang="en"
      className={`${geistSans.variable} ${geistMono.variable} h-full antialiased`}
    >
      <head>
        <script dangerouslySetInnerHTML={{ __html: themeScript }} />
      </head>
      <body className="min-h-full flex flex-col">{children}</body>
    </html>
  );
}
